// Create utility class for managing the comprehensive question bank
class ComprehensiveQuestionManager {
    constructor() {
        this.questions = COMPREHENSIVE_USMLE_QUESTION_BANK;
        this.categories = this.getAllCategories();
        this.subjects = this.getAllSubjects();
    }

    getAllQuestions() {
        return this.questions;
    }

    getQuestionsByCategory(category) {
        return this.questions.filter(q => q.category === category);
    }

    getQuestionsBySubject(subject) {
        return this.questions.filter(q => q.subject === subject);
    }

    getAllCategories() {
        return [...new Set(this.questions.map(q => q.category))].sort();
    }

    getAllSubjects() {
        return [...new Set(this.questions.map(q => q.subject))].sort();
    }

    getRandomQuestions(count, category = null, subject = null) {
        let sourceQuestions = this.questions;
        
        if (category) {
            sourceQuestions = sourceQuestions.filter(q => q.category === category);
        }
        
        if (subject) {
            sourceQuestions = sourceQuestions.filter(q => q.subject === subject);
        }
        
        const shuffled = [...sourceQuestions].sort(() => Math.random() - 0.5);
        return shuffled.slice(0, Math.min(count, sourceQuestions.length));
    }

    getQuestionById(id) {
        return this.questions.find(q => q.id === id);
    }

    searchQuestions(searchTerm) {
        const term = searchTerm.toLowerCase();
        return this.questions.filter(q => 
            q.questionText.toLowerCase().includes(term) ||
            q.explanation.toLowerCase().includes(term) ||
            q.options.some(option => option.toLowerCase().includes(term))
        );
    }

    getStatistics() {
        return {
            totalQuestions: this.questions.length,
            categoryCounts: this.categories.reduce((acc, cat) => {
                acc[cat] = this.getQuestionsByCategory(cat).length;
                return acc;
            }, {}),
            subjectCounts: this.subjects.reduce((acc, subj) => {
                acc[subj] = this.getQuestionsBySubject(subj).length;
                return acc;
            }, {})
        };
    }
}

